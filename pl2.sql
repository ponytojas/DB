CREATE TABLE GRUPO(
	NOMBRE_GRUPO varchar(30) PRIMARY KEY,
	GENERO_GRUPO varchar(20)
);

CREATE TABLE USUARIO(
	DNI_USUARIO varchar (10) PRIMARY KEY ,
	EMAIL_USUARIO varchar (30),
	NOMBRE_USUARIO varchar (15),
	APELLIDOS_USUARIO varchar (30)
);

CREATE TABLE CONCIERTO(
	ID_CONCIERTO SERIAL PRIMARY KEY NOT NULL,
	NOMBRE_GRUPO varchar(30),
	FECHA_CONCIERTO date,
	CIUDAD_CONCIERTO varchar (20),

	CONSTRAINT FK_NOMBRE_GRUPO_CONCIERTO FOREIGN KEY (NOMBRE_GRUPO) REFERENCES GRUPO (NOMBRE_GRUPO)
);

CREATE TABLE INSTRUMENTO(
	NOMBRE_INSTRUMENTO varchar (20) PRIMARY KEY,
	TIPO_INSTRUMENTO varchar (20)
);

CREATE TABLE MUSICO(
	DNI_MUSICO varchar (10) PRIMARY KEY,
	NOMBRE_MUSICO varchar (10),
	APELLIDOS_MUSICO varchar (30),
	DIRECCION_MUSICO varchar (30),
	CODIGO_POSTAL int,
	PROVINCIA varchar (30),
	NUMERO_TELEFONO int
);

CREATE TABLE MUSICO_GRUPO(
	DNI_MUSICO varchar(10) PRIMARY KEY,
	NOMBRE_GRUPO varchar(30),
	
	CONSTRAINT FK_DNI_MUSICO_INSTRUMENTO FOREIGN KEY (DNI_MUSICO) REFERENCES MUSICO (DNI_MUSICO),
	CONSTRAINT FK_NOMBRE_GRUPO FOREIGN KEY (NOMBRE_GRUPO) REFERENCES GRUPO (NOMBRE_GRUPO)
);

CREATE TABLE INSTRUMENTO_MUSICO(
	ID_INSTRUMENTO_MUSICO SERIAL PRIMARY KEY,
	DNI_MUSICO varchar(10),
	NOMBRE_INSTRUMENTO varchar(20),
	
	CONSTRAINT FK_DNI_MUSICO_INSTRUMENTO FOREIGN KEY (DNI_MUSICO) REFERENCES MUSICO (DNI_MUSICO),
	CONSTRAINT FK_ID_INSTRUMENTO FOREIGN KEY (NOMBRE_INSTRUMENTO) REFERENCES INSTRUMENTO (NOMBRE_INSTRUMENTO)
);

CREATE TABLE COMPOSITOR(
	DNI_MUSICO varchar(10) PRIMARY KEY,

	CONSTRAINT FK_DNI_MUSICO_INSTRUMENTO FOREIGN KEY (DNI_MUSICO) REFERENCES MUSICO (DNI_MUSICO)

);

CREATE TABLE DISCO(
	ID_DISCO SERIAL PRIMARY KEY ,
	FECHA_EDICION date,
	TITULO_DISCO varchar(50),
	GENERO_DISCO varchar(15)
);

CREATE TABLE DISCO_DIGITAL(
	ID_DISCO SERIAL PRIMARY KEY,
	TAMAÑO_DISCO SMALLINT,
	PRECIO_DISCO MONEY,
	
	CONSTRAINT FK_ID_DISCO FOREIGN KEY (ID_DISCO) REFERENCES DISCO (ID_DISCO)
);

CREATE TABLE DISCO_FISICO(
	ID_DISCO SERIAL PRIMARY KEY,
	PRECIO_DISCO MONEY,
	
	CONSTRAINT FK_ID_DISCO FOREIGN KEY (ID_DISCO) REFERENCES DISCO (ID_DISCO)
);

CREATE TABLE DISCO_GRUPO(
	ID_DISCO int PRIMARY KEY,
	NOMBRE_GRUPO varchar(30),
	
	CONSTRAINT FK_NOMBRE_GRUPO FOREIGN KEY (NOMBRE_GRUPO) REFERENCES GRUPO (NOMBRE_GRUPO),
	CONSTRAINT FK_ID_DISCO FOREIGN KEY (ID_DISCO) REFERENCES DISCO (ID_DISCO)
);

CREATE TABLE CANCION(
	ID_CANCION SERIAL PRIMARY KEY,
	DURACION float ,
	NOMBRE_CANCION varchar(40)
);

CREATE TABLE CANCION_DISCO(
	ID_CANCION int PRIMARY KEY,
	ID_DISCO int,
	
	CONSTRAINT FK_ID_CANCION FOREIGN KEY (ID_CANCION) REFERENCES CANCION (ID_CANCION),
	CONSTRAINT FK_ID_DISCO FOREIGN KEY (ID_DISCO) REFERENCES DISCO (ID_DISCO)
);

CREATE TABLE COMPOSITOR_CANCION(
	ID_CANCION int PRIMARY KEY,
	DNI_MUSICO varchar(10)  REFERENCES COMPOSITOR (DNI_MUSICO),
	
	CONSTRAINT FK_ID_CANCION FOREIGN KEY (ID_CANCION) REFERENCES CANCION (ID_CANCION)
);

CREATE TABLE COMPRA(
	ID_COMPRA SERIAL PRIMARY KEY NOT NULL,
	PUNTOS_GANADOS int,
	PUNTOS_GASTADOS int
);

CREATE TABLE COMPRA_DISCO(
	ID_COMPRA SERIAL PRIMARY KEY,
	ID_DISCO SERIAL,
	
	CONSTRAINT FK_ID_COMPRA FOREIGN KEY (ID_COMPRA) REFERENCES COMPRA (ID_COMPRA),
	CONSTRAINT FK_CODIGO_DISCO FOREIGN KEY (ID_DISCO) REFERENCES DISCO (ID_DISCO)
);


CREATE TABLE ENTRADA(
	ID_ENTRADA SERIAL PRIMARY KEY,
	ID_CONCIERTO SERIAL,

	CONSTRAINT FK_ID_CONCIERTO FOREIGN KEY (ID_CONCIERTO) REFERENCES CONCIERTO (ID_CONCIERTO)
);

CREATE TABLE PRECIO_ENTRADA(
	ID_ENTRADA SERIAL PRIMARY KEY,
	PRECIO_ENTRADA MONEY NOT NULL,
	
	CONSTRAINT FK_ID_ENTRADA FOREIGN KEY (ID_ENTRADA) REFERENCES ENTRADA (ID_ENTRADA)
);

CREATE TABLE COMPRA_ENTRADA(
	ID_COMPRA SERIAL PRIMARY KEY REFERENCES COMPRA (ID_COMPRA),
	ID_ENTRADA SERIAL REFERENCES ENTRADA (ID_ENTRADA)
);

CREATE TABLE USUARIO_COMPRA (
	ID_USUARIO_COMPRA SERIAL PRIMARY KEY,
	DNI_USUARIO_COMPRA varchar(10),
	ID_COMPRA integer,
	PUNTOS_GASTADOS smallint not null,
	PUNTOS_GANADOS smallint not null,
	OPINION smallint CHECK (OPINION BETWEEN 1 AND 10),
	
	CONSTRAINT FK_DNI_USUARIO_COMPRA FOREIGN KEY (DNI_USUARIO_COMPRA) REFERENCES USUARIO (DNI_USUARIO),
	CONSTRAINT FK_ID_COMPRA FOREIGN KEY (ID_COMPRA) REFERENCES COMPRA (ID_COMPRA)
);

CREATE TABLE PAIS_CONCIERTO(
	CIUDAD_CONCIERTO varchar (20) PRIMARY KEY,
	PAIS_CONCIERTO varchar(20) 
);

