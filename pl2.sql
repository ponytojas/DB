CREATE TABLE GRUPO(
	ID_GRUPO SERIAL PRIMARY KEY,
	GENERO_GRUPO varchar(20),
	NOMBRE_GRUPO varchar(30)
);

CREATE TABLE USUARIO(
	DNI_USUARIO varchar (10) PRIMARY KEY ,
	EMAIL_USUARIO varchar (30),
	NOMBRE_USUARIO varchar (15),
	APELLIDOS_USUARIO varchar (30)
);

CREATE TABLE CONCIERTO(
	CODIGO_CONCIERTO SERIAL PRIMARY KEY,
	ID_GRUPO int,
	PAIS_CONCIERTO varchar(20),
	CIUDAD_CONCIERTO varchar (20),

	CONSTRAINT FK_ID_GRUPO_CONCIERTO FOREIGN KEY (ID_GRUPO) REFERENCES GRUPO (ID_GRUPO)
);

CREATE TABLE INSTRUMENTO(
	ID_INSTRUMENTO SERIAL PRIMARY KEY,
	NOMBRE_INSTRUMENTO varchar (20),
	TIPO_INSTRUMENTO varchar (20)
);

CREATE TABLE MUSICO(
	DNI_MUSICO varchar (10) PRIMARY KEY,
	NOMBRE_MUSICO varchar (10),
	APELLIDOS_MUSICO varchar (30),
	DIRECCION_MUSICO varchar (30),
	CODIGO_POSTAL int,
	PROVINCIA varchar (30),
	NUMERO_TELEFONO int
);

CREATE TABLE MUSICO_GRUPO(
	ID_MUSICO_GRUPO SERIAL PRIMARY KEY,
	DNI_MUSICO varchar(10),
	ID_GRUPO int,
	
	CONSTRAINT FK_DNI_MUSICO_INSTRUMENTO FOREIGN KEY (DNI_MUSICO) REFERENCES MUSICO (DNI_MUSICO),
	CONSTRAINT FK_ID_GRUPO FOREIGN KEY (ID_GRUPO) REFERENCES GRUPO (ID_GRUPO)
);

CREATE TABLE INSTRUMENTO_MUSICO(
	ID_INSTRUMENTO_MUSICO SERIAL PRIMARY KEY,
	DNI_MUSICO varchar(10),
	ID_INSTRUMENTO int,
	
	CONSTRAINT FK_DNI_MUSICO_INSTRUMENTO FOREIGN KEY (DNI_MUSICO) REFERENCES MUSICO (DNI_MUSICO),
	CONSTRAINT FK_ID_INSTRUMENTO FOREIGN KEY (ID_INSTRUMENTO) REFERENCES INSTRUMENTO (ID_INSTRUMENTO)
);

CREATE TABLE COMPOSITOR(
	ID_COMPOSITOR SERIAL PRIMARY KEY,
	DNI_MUSICO varchar(10),

	CONSTRAINT FK_DNI_MUSICO_INSTRUMENTO FOREIGN KEY (DNI_MUSICO) REFERENCES MUSICO (DNI_MUSICO)

);

CREATE TABLE DISCO(
	ID_DISCO SERIAL PRIMARY KEY ,
	FECHA_EDICION date,
	TITULO_DISCO varchar(50),
	GENERO_DISCO varchar(15),
	ES_FISICO boolean default null,
	ES_DIGITAL boolean default null,	
	TAMAÑO_DISCO smallint default null,

	CONSTRAINT IS_DIGITAL_FALSE  CHECK ( (NOT ES_DIGITAL) OR (TAMAÑO_DISCO IS NOT NULL))
);

CREATE TABLE DISCO_GRUPO(
	ID_DISCO_GRUPO SERIAL PRIMARY KEY,
	ID_GRUPO int,
	ID_DISCO int,
	
	CONSTRAINT FK_ID_GRUPO FOREIGN KEY (ID_GRUPO) REFERENCES GRUPO (ID_GRUPO),
	CONSTRAINT FK_ID_DISCO FOREIGN KEY (ID_DISCO) REFERENCES DISCO (ID_DISCO)
);

CREATE TABLE CANCION(
	ID_CANCION SERIAL PRIMARY KEY,
	DURACION float ,
	NOMBRE_CANCION varchar(40)
);

CREATE TABLE CANCION_DISCO(
	ID_CANCION_DISCO SERIAL PRIMARY KEY,
	ID_CANCION int,
	ID_DISCO int,
	
	CONSTRAINT FK_ID_CANCION FOREIGN KEY (ID_CANCION) REFERENCES CANCION (ID_CANCION),
	CONSTRAINT FK_ID_DISCO FOREIGN KEY (ID_DISCO) REFERENCES DISCO (ID_DISCO)
);

CREATE TABLE COMPOSITOR_CANCION(
	ID_COMPOSITOR_CANCION SERIAL PRIMARY KEY,
	ID_CANCION int,
	ID_COMPOSITOR int,
	
	CONSTRAINT FK_ID_CANCION FOREIGN KEY (ID_CANCION) REFERENCES CANCION (ID_CANCION),
	CONSTRAINT FK_ID_DISCO FOREIGN KEY (ID_COMPOSITOR) REFERENCES COMPOSITOR (ID_COMPOSITOR)
);



CREATE TABLE COMPRA(
	CODIGO_COMPRA SERIAL PRIMARY KEY not null,
	ID_DISCO int default null,
	CODIGO_CONCIERTO SERIAL,
	ES_COMPRA_DISCO boolean default false,
	ES_COMPRA_ENTRADA boolean default false,
	CANTIDAD_ENTRADA smallint CHECK (CANTIDAD_ENTRADA BETWEEN 1 AND 6),

	CONSTRAINT IS_COMPRA_DISCO_FALSE  CHECK ( (NOT ES_COMPRA_DISCO) OR (ID_DISCO IS NOT NULL)),
	CONSTRAINT IS_COMPRA_CONCIERTO_FALSE  CHECK ( (NOT ES_COMPRA_ENTRADA) OR
												 (CODIGO_CONCIERTO  IS NOT NULL) AND 
												 (CANTIDAD_ENTRADA IS NOT NULL)),
	
	CONSTRAINT FK_ID_DISCO_COMPRA FOREIGN KEY (ID_DISCO) REFERENCES DISCO (ID_DISCO),
	CONSTRAINT FK_CODIGO_CONCIERTO FOREIGN KEY (CODIGO_CONCIERTO) REFERENCES CONCIERTO (CODIGO_CONCIERTO)
);

CREATE TABLE ENTRADA(
	CODIGO_ENTRADA SERIAL PRIMARY KEY,
	PRECIO_ENTRADA money not null,
	CODIGO_CONCIERTO SERIAL,

	CONSTRAINT FK_CODIGO_CONCIERTO FOREIGN KEY (CODIGO_CONCIERTO) REFERENCES CONCIERTO (CODIGO_CONCIERTO)
);

CREATE TABLE COMPRA_ENTRADA(
	CODIGO_COMPRA_ENTRADA SERIAL PRIMARY KEY,
	CODIGO_COMPRA int,
	CODIGO_ENTRADA int,
	
	CONSTRAINT FK_CODIGO_COMPRA FOREIGN KEY (CODIGO_COMPRA) REFERENCES COMPRA (CODIGO_COMPRA),
	CONSTRAINT FK_CODIGO_ENTRADA FOREIGN KEY (CODIGO_ENTRADA) REFERENCES ENTRADA (CODIGO_ENTRADA)
);

CREATE TABLE USUARIO_COMPRA (
	ID_USUARIO_COMPRA SERIAL PRIMARY KEY,
	DNI_USUARIO_COMPRA varchar(10),
	CODIGO_COMPRA integer,
	PUNTOS_GASTADOS smallint not null,
	PUNTOS_GANADOS smallint not null,
	OPINION smallint CHECK (OPINION BETWEEN 1 AND 10),
	
	CONSTRAINT FK_DNI_USUARIO_COMPRA FOREIGN KEY (DNI_USUARIO_COMPRA) REFERENCES USUARIO (DNI_USUARIO),
	CONSTRAINT FK_CODIGO_COMPRA FOREIGN KEY (CODIGO_COMPRA) REFERENCES COMPRA (CODIGO_COMPRA)
);

